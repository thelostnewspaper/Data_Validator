@dataclass(frozen=True)
class AirflowConnectionFix:
    """
    Suggests creating/updating an Airflow connection.

    Rendered as a special card in the panel with a "Create Connection" button
    that calls the Airflow REST API via the engine connector.
    """
    conn_id: str
    conn_type: str
    host: str = ""
    port: int = 0
    schema: str = ""
    login: str = ""
    extra: dict[str, Any] = field(default_factory=dict)
    description: str = ""

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


# ---------------------------------------------------------------------------
# Aggregated validation result
# ---------------------------------------------------------------------------

@dataclass
class ValidationSummary:
    """Counts by status for the status-bar display."""
    total: int = 0
    passed: int = 0
    warnings: int = 0
    failures: int = 0


@dataclass
class ValidationResult:
    """
    Complete validation output for a single file.

    This is the top-level response for the `validate` JSON-RPC method.
    """
    file_path: str
    checks: list[CheckResult] = field(default_factory=list)
    fixes: list[SuggestedFix] = field(default_factory=list)
    connection_fixes: list[AirflowConnectionFix] = field(default_factory=list)
    summary: ValidationSummary = field(default_factory=ValidationSummary)

    def compute_summary(self) -> None:
        """Recompute summary counts from the checks list."""
        self.summary = ValidationSummary(
            total=len(self.checks),
            passed=sum(1 for c in self.checks if c.status == CheckStatus.PASS),
            warnings=sum(1 for c in self.checks if c.status == CheckStatus.WARN),
            failures=sum(1 for c in self.checks if c.status == CheckStatus.FAIL),
        )

    def to_dict(self) -> dict[str, Any]:
        self.compute_summary()
        return {
            "file_path": self.file_path,
            "checks": [c.to_dict() for c in self.checks],
            "fixes": [f.to_dict() for f in self.fixes],
            "connection_fixes": [cf.to_dict() for cf in self.connection_fixes],
            "summary": asdict(self.summary),
        }


# ---------------------------------------------------------------------------
# AI remediation option (one variant: low / medium / high)
# ---------------------------------------------------------------------------

@dataclass
class CodeChange:
    """A single file change within a remediation option."""
    file_path: str
    old_content: str
    new_content: str
    description: str = ""


@dataclass
class RemediationOption:
    """
    One AI-generated fix variant.

    The engine produces up to 3 variants (low/medium/high impact).
    Each MUST pass ast.parse() + static_checks before being shown to the user.
    Failed variants are still returned with failed=True so the UI can explain why.

    Attributes:
        impact:           low / medium / high.
        title:            Short title for the fix card.
        root_cause:       Explanation of why the issue occurs.
        fix_explanation:  What this fix does and why.
        changes:          List of code changes.
        dag_code:         The complete fixed DAG code.
        diff:             Unified diff of the full file.
        failed:           True if the variant failed the static-check gate.
        failure_reason:   Why the gate rejected this variant.
        checks_after:     Validation result after applying the fix (for cascade display).
    """
    impact: FixImpact
    title: str
    root_cause: str = ""
    fix_explanation: str = ""
    changes: list[CodeChange] = field(default_factory=list)
    dag_code: str = ""
    diff: str = ""
    failed: bool = False
    failure_reason: str = ""
    checks_after: list[dict[str, Any]] = field(default_factory=list)

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        d["impact"] = self.impact.value
        return d
