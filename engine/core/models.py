"""
Data models — the stable contract between the Python engine and the TS extension.

All models serialize to JSON via dataclasses.asdict() for the JSON-RPC layer.
These are the ONLY types that cross the process boundary.
"""

from __future__ import annotations

import enum
from dataclasses import dataclass, field, asdict
from typing import Any


class CheckStatus(str, enum.Enum):
    """Severity level of a validation check result."""
    PASS = "pass"
    WARN = "warn"
    FAIL = "fail"


class CheckCategory(str, enum.Enum):
    """Logical grouping for check results displayed in the panel."""
    SYNTAX = "syntax"
    STRUCTURE = "structure"
    VARIABLES = "variables"
    COLUMNS = "columns"
    DEPENDENCIES = "dependencies"
    CONNECTIONS = "connections"
    SECURITY = "security"
    BEST_PRACTICES = "best_practices"


class FixImpact(str, enum.Enum):
    """Impact level for AI remediation variants."""
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"


# ---------------------------------------------------------------------------
# Core check result — one per rule evaluation
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class CheckResult:
    """
    A single validation finding.

    Attributes:
        id:          Unique check ID (e.g. AFW001).
        status:      pass / warn / fail.
        category:    Logical group for panel rendering.
        message:     One-line summary shown in diagnostics hover.
        detail:      Extended explanation for the panel card.
        line:        1-indexed line in the source file (0 if unknown).
        column:      0-indexed column (0 if unknown).
        end_line:    End line for range highlighting (same as line if single-line).
        end_column:  End column for range highlighting.
        source_rule: Human-readable rule name for documentation links.
    """
    id: str
    status: CheckStatus
    category: CheckCategory
    message: str
    detail: str = ""
    line: int = 0
    column: int = 0
    end_line: int = 0
    end_column: int = 0
    source_rule: str = ""

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        d["status"] = self.status.value
        d["category"] = self.category.value
        return d


# ---------------------------------------------------------------------------
# Deterministic fix suggestion (no LLM needed)
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class SuggestedFix:
    """
    A deterministic, LLM-free fix suggestion.

    Generated by fuzzy-matching (difflib SequenceMatcher >= 0.80), pattern
    detection (duplicate column removal), or structural fixes.

    Attributes:
        check_id:    The CheckResult.id this fix addresses.
        description: Human-readable fix description for the lightbulb menu.
        old_text:    The exact text to replace in the source.
        new_text:    The replacement text.
        diff:        Unified diff string for preview in the panel.
        confidence:  Match confidence 0.0–1.0 (from SequenceMatcher).
        line:        Line where the fix applies.
        column:      Column where the fix applies.
    """
    check_id: str
    description: str
    old_text: str
    new_text: str
    diff: str = ""
    confidence: float = 1.0
    line: int = 0
    column: int = 0

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


# ---------------------------------------------------------------------------
